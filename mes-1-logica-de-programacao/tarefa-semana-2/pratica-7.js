function helloWorld3idiomas(idioma) {
    if (idioma == "en") {
        console.log('Hello World!!! ');

    } else if (idioma == "es") {
        console.log('Hola mundo!!!');

    } else if (idioma == "it") {
        console.log('Ciao mondo!!!')
    }
    else

    console.log('Olá mundo!!!')
}

helloWorld3idiomas("")

// "en" retorna:    Hello World!!!
// "es" retorna:    Hola mundo!!!
// "it" retorna:    Ciao mondo!!!
// "  " retorna:    Olá mundo!!!


